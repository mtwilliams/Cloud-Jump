function love.conf(t)
	t.title = "Cloud Jump"        -- The title of the window the game is in (string)
	t.author = "Michael Williams" -- The author of the game (string)
	t.identity = nil              -- The name of the save directory (string)
	t.screen.width = 850          -- The window width (number)
	t.screen.height = 450         -- The window height (number)
	t.screen.fullscreen = false   -- Enable fullscreen (boolean)
	t.screen.vsync = true         -- Enable vertical sync (boolean)
	t.console = false
end
